# = Assignment Operator
x = 5
print("= : x =", x)  # x = 5

# += Add and Assign
x += 3  # x = x + 3
print("+= : x =", x)  # x = 8

# -= Subtract and Assign
x -= 3  # x = x - 3
print("-= : x =", x)  # x = 5

# *= Multiply and Assign
x *= 3  # x = x * 3
print("*= : x =", x)  # x = 15

# /= Divide and Assign
x /= 3  # x = x / 3
print("/= : x =", x)  # x = 5.0 (note: becomes float)

# %= Modulus and Assign
x %= 3  # x = x % 3
print("%= : x =", x)  # x = 2.0

# //= Floor Divide and Assign
x = 10
x //= 3  # x = x // 3
print("//= : x =", x)  # x = 3

# **= Power and Assign
x **= 3  # x = x ** 3
print("**= : x =", x)  # x = 27